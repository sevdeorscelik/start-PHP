## Incrementing & Decrementing Operators ##

//artirma ve azaltma operatorleri

//z.B.1
$a = 5;

echo $a++ ; //5  -> önce yazdirip sonra artirdigi icin sayidaki degisikligi görmüyoruz

echo $a; //6 -> önce artip sonra yazdirir. sayinin arttigini görürüz.

//z.B.2
$b = 2;
echo --$b; //1
echo $b--; //1


//z.B.3
$age = 28;
echo --$age; //27
echo $age++; //27
echo ++$age; //29
echo $age--; //29





